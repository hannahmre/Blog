function color_change(tab) {
    if (tab == 'about') {
        document.body.style.backgroundColor = "#ffccff";
    } else if (tab == 'travel') {
        document.body.style.backgroundColor = "#ccccff";
    } else if (tab == 'recipes') {
        document.body.style.backgroundColor = "#ccffff";
    } else {
        document.body.style.backgroundColor = "#ccffcc";
    }

}